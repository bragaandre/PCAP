#### Double Parameters Functions ###


def message(what, number):
    print("Enter", what, "number", number)

def myFunction(a, b, c):
    print(a, b, c)

def introduction(First_Name, Last_Name):
    print('My guest needs no introduction w/David Letterman')
    print('Ladies and Gentleman, a big welcome to',First_Name, Last_Name)


message("telephone", 11)
message("price", 5)
message("number", "number")

myFunction(1, 2, 3)


introduction('Robert','Downey Jr.')
introduction('Steve','Jobs')

